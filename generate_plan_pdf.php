$severityName = (string) ($data['severity'] ?? 'Severity');

$scenarioPal = scenario_palette($scenarioName);
$severityPal = severity_palette($severityName);

$pdf = new HandaVisPlanPDF();
$pdf->scenarioPalette = $scenarioPal;
$pdf->severityPalette = $severityPal;
$pdf->scenarioName = $scenarioName;
