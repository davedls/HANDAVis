function safe_text($value): string {
    $text = is_scalar($value) ? (string) $value : '';
    $text = preg_replace('/\s+/u', ' ', trim($text));
    $converted = @iconv('UTF-8', 'windows-1252//TRANSLIT//IGNORE', $text);
    return $converted !== false ? $converted : $text;
}

function safe_lines($items): array {
    if (!is_array($items)) {
        return [];
    }
    $out = [];
    foreach ($items as $item) {
        $line = safe_text($item);
        if ($line !== '') {
            $out[] = $line;
        }
    }
    return $out;
}

function safe_phone_link($number): string {
    $clean = preg_replace('/[^0-9+]/', '', (string) $number);
    if ($clean === '') {
        return '';
    }
    if ($clean[0] !== '+') {
        $clean = '+' . ltrim($clean, '+');
    }
    return 'tel:' . $clean;
}

