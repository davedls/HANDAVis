draw_info_row($pdf, 'Action Note', (string) ($decision['actionNote'] ?? ''), $scenarioPal['accent']);
draw_info_row($pdf, 'Movement Advice', trim((string) ($decision['movement'] ?? '') . ' - ' . (string) ($decision['movementNote'] ?? '')), $scenarioPal['accent']);
