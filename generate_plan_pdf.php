class HandaVisPlanPDF extends FPDF {
    public array $scenarioPalette = [];
    public array $severityPalette = [];
    public string $scenarioName = 'Scenario';
    public string $severityName = 'Severity';

    public function Header(): void {
        $this->SetFillColor(11, 20, 33);
        $this->Rect(0, 0, 210, 18, 'F');

        $accent = $this->scenarioPalette['accent'] ?? [95, 216, 255];
        $this->SetFillColor($accent[0], $accent[1], $accent[2]);
        $this->Rect(0, 0, 210, 2.5, 'F');

        $this->SetFont('Helvetica', 'B', 11);
        $this->SetTextColor(255, 255, 255);
        $this->SetXY(14, 6);
        $this->Cell(90, 6, 'HANDAVis Emergency Plan', 0, 0, 'L');

        $this->SetFont('Helvetica', '', 8.5);
        $this->SetTextColor(200, 224, 241);
        $this->Cell(0, 6, safe_text($this->scenarioName . ' • ' . $this->severityName), 0, 0, 'R');

        $this->Ln(8);
    }

    public function Footer(): void {
        $this->SetY(-12);
        $this->SetDrawColor(214, 225, 236);
        $this->Line(14, $this->GetY(), 196, $this->GetY());
        $this->SetY(-10);
        $this->SetFont('Helvetica', '', 8.5);
        $this->SetTextColor(110, 118, 133);
        $this->Cell(0, 5, 'Generated by HANDAVis - Page ' . $this->PageNo(), 0, 0, 'C');
    }
}

function ensure_space(FPDF $pdf, float $needed): void {
    if ($pdf->GetY() + $needed > ($pdf->GetPageHeight() - 16)) {
        $pdf->AddPage();
    }
}

function fill_rect(FPDF $pdf, float $x, float $y, float $w, float $h, array $rgb): void {
    $pdf->SetFillColor($rgb[0], $rgb[1], $rgb[2]);
    $pdf->Rect($x, $y, $w, $h, 'F');
}

function draw_chip(FPDF $pdf, float $x, float $y, float $w, string $text, array $fill, array $textColor = [255, 255, 255]): void {
    $pdf->SetXY($x, $y);
    $pdf->SetFillColor($fill[0], $fill[1], $fill[2]);
    $pdf->SetTextColor($textColor[0], $textColor[1], $textColor[2]);
    $pdf->SetFont('Helvetica', 'B', 8.5);
    $pdf->Cell($w, 7, safe_text($text), 0, 0, 'C', true);
}

function draw_hero(FPDF $pdf, array $data, array $scenarioPal, array $severityPal): void {
    $x = 14;
    $y = 24;
    $w = 182;
    $h = 36;

    fill_rect($pdf, $x, $y, $w, $h, [15, 24, 38]);
    fill_rect($pdf, $x, $y, 5.5, $h, $scenarioPal['accent']);

    $pdf->SetXY($x + 10, $y + 6);
    $pdf->SetTextColor(255, 255, 255);
    $pdf->SetFont('Helvetica', 'B', 18);
    $pdf->Cell(98, 8, safe_text(($data['scenario'] ?? 'Emergency') . ' Response Plan'), 0, 1, 'L');

    $pdf->SetX($x + 10);
    $pdf->SetFont('Helvetica', '', 9.5);
    $pdf->SetTextColor(201, 222, 239);
    $subtitle = ($data['location'] ?? '') . ' • ' . ($data['timeWindow'] ?? '') . ' • Generated ' . ($data['generatedAt'] ?? date('Y-m-d H:i:s'));
    $pdf->Cell(110, 6, safe_text($subtitle), 0, 0, 'L');

    draw_chip($pdf, $x + $w - 50, $y + 7, 38, strtoupper((string) ($data['severity'] ?? 'Severity')), $severityPal['accent']);
    draw_chip($pdf, $x + $w - 50, $y + 18, 38, strtoupper((string) ($data['scenario'] ?? 'Scenario')), $scenarioPal['chip']);

    $pdf->SetDrawColor(58, 81, 103);
    $pdf->Line($x + 10, $y + 27, $x + 120, $y + 27);

    $pdf->SetXY($x + 10, $y + 29);
    $pdf->SetTextColor(154, 187, 212);
    $pdf->SetFont('Helvetica', '', 8.5);
    $pdf->Cell(120, 5, 'Prepared for quick reference, route guidance, and local emergency response coordination.', 0, 0, 'L');

    $pdf->SetY($y + $h + 8);
}

function draw_section_title(FPDF $pdf, string $title, array $accent): void {
    ensure_space($pdf, 12);
    $y = $pdf->GetY();
    fill_rect($pdf, 14, $y, 4, 8, $accent);
    $pdf->SetXY(21, $y + 0.5);
    $pdf->SetFont('Helvetica', 'B', 12);
    $pdf->SetTextColor(20, 33, 46);
    $pdf->Cell(0, 7, safe_text($title), 0, 1, 'L');
    $pdf->Ln(1.5);
}

function draw_metric_box(FPDF $pdf, float $x, float $y, float $w, string $label, string $value, array $fill, array $valueColor): void {
    fill_rect($pdf, $x, $y, $w, 19, $fill);
    $pdf->SetDrawColor(223, 232, 242);
    $pdf->Rect($x, $y, $w, 19);

    $pdf->SetXY($x + 4, $y + 3);
    $pdf->SetFont('Helvetica', 'B', 8.5);
    $pdf->SetTextColor(102, 118, 134);
    $pdf->Cell($w - 8, 4.5, safe_text($label), 0, 1, 'L');

    $pdf->SetX($x + 4);
    $pdf->SetFont('Helvetica', 'B', 12.5);
    $pdf->SetTextColor($valueColor[0], $valueColor[1], $valueColor[2]);
    $pdf->Cell($w - 8, 6, safe_text($value), 0, 0, 'L');
}

function draw_info_row(FPDF $pdf, string $label, string $value, array $accent): void {
    if ($value === '') {
        return;
    }
    ensure_space($pdf, 8);
    $x = 14;
    $y = $pdf->GetY();
    $pdf->SetDrawColor(232, 238, 244);
    $pdf->Line($x, $y + 7.5, 196, $y + 7.5);

    $pdf->SetXY($x, $y + 1);
    $pdf->SetFont('Helvetica', 'B', 9);
    $pdf->SetTextColor($accent[0], $accent[1], $accent[2]);
    $pdf->Cell(38, 5, safe_text($label), 0, 0, 'L');

    $pdf->SetFont('Helvetica', '', 10);
    $pdf->SetTextColor(43, 52, 61);
    $pdf->SetXY($x + 38, $y + 0.2);
    $pdf->MultiCell(144, 5.6, safe_text($value), 0, 'L');
    $pdf->Ln(1.5);
}

function draw_bullet_list(FPDF $pdf, array $items, array $bulletColor): void {
    if (!$items) {
        draw_info_row($pdf, 'Status', 'No items available.', $bulletColor);
        return;
    }
    foreach ($items as $item) {
        ensure_space($pdf, 8);
        $x = 14;
        $y = $pdf->GetY();
        $pdf->SetXY($x, $y + 0.5);
        $pdf->SetFont('Helvetica', 'B', 12);
        $pdf->SetTextColor($bulletColor[0], $bulletColor[1], $bulletColor[2]);
        $pdf->Cell(6, 5, chr(149), 0, 0, 'C');

        $pdf->SetFont('Helvetica', '', 10);
        $pdf->SetTextColor(43, 52, 61);
        $pdf->SetXY($x + 7, $y);
        $pdf->MultiCell(175, 5.7, safe_text($item), 0, 'L');
        $pdf->Ln(0.8);
    }
}

function draw_sub_card(FPDF $pdf, string $title, array $titleFill, callable $content): void {
    ensure_space($pdf, 18);
    $x = 14;
    $y = $pdf->GetY();
    fill_rect($pdf, $x, $y, 182, 8, $titleFill);
    $pdf->SetXY($x + 4, $y + 1.3);
    $pdf->SetFont('Helvetica', 'B', 10);
    $pdf->SetTextColor(255, 255, 255);
    $pdf->Cell(174, 5, safe_text($title), 0, 1, 'L');
    $pdf->Ln(1.5);
    $content($pdf);
    $pdf->Ln(2);
}

function draw_place_entry(FPDF $pdf, array $place, array $scenarioPal): void {
    $name = safe_text($place['name'] ?? 'Support Point');
    $address = safe_text($place['address'] ?? '');
    $phone = safe_text($place['phone'] ?? '');
    $note = safe_text($place['note'] ?? '');

    ensure_space($pdf, 18);
    $x = 14;
    $y = $pdf->GetY();
    fill_rect($pdf, $x, $y, 182, 1.6, $scenarioPal['accent']);

    $pdf->SetXY($x, $y + 3);
    $pdf->SetFont('Helvetica', 'B', 10.5);
    $pdf->SetTextColor(20, 33, 46);
    $pdf->Cell(0, 5, $name, 0, 1, 'L');

    if ($address !== '') {
        $pdf->SetX($x);
        $pdf->SetFont('Helvetica', '', 9.5);
        $pdf->SetTextColor(78, 92, 106);
        $pdf->MultiCell(182, 5, $address, 0, 'L');
    }

    if ($phone !== '' || $note !== '') {
        $pdf->SetX($x);
        $pdf->SetFont('Helvetica', '', 9);
        $pdf->SetTextColor(96, 107, 120);
        $info = [];
        if ($phone !== '') {
            $info[] = 'Desk: ' . $phone;
        }
        if ($note !== '') {
            $info[] = $note;
        }
        $pdf->MultiCell(182, 4.8, safe_text(implode(' • ', $info)), 0, 'L');
    }

    $pdf->Ln(1);
}

function draw_contact_entry(FPDF $pdf, array $contact, array $severityPal): void {
    $name = safe_text($contact['name'] ?? 'Emergency Contact');
    $number = safe_text($contact['number'] ?? '');
    $detail = safe_text($contact['detail'] ?? '');
    $telLink = safe_phone_link($number);

    ensure_space($pdf, 16);
    $x = 14;
    $y = $pdf->GetY();
    fill_rect($pdf, $x, $y, 182, 1.6, $severityPal['accent']);

    $pdf->SetXY($x, $y + 3);
    $pdf->SetFont('Helvetica', 'B', 10.5);
    $pdf->SetTextColor(20, 33, 46);
    $pdf->Cell(0, 5, $name, 0, 1, 'L');

    $pdf->SetX($x);
    $pdf->SetFont('Helvetica', '', 9.5);
    $pdf->SetTextColor(46, 79, 123);
    if ($number !== '') {
        $pdf->Cell(70, 5, $number, 0, 0, 'L', false, $telLink ?: '');
    }
    $pdf->SetTextColor(96, 107, 120);
    if ($detail !== '') {
        $pdf->MultiCell(112, 5, $detail, 0, 'L');
    } else {
        $pdf->Ln(5);
    }

    if ($telLink !== '') {
        $pdf->SetX($x);
        $pdf->SetFont('Helvetica', 'B', 8.5);
        $pdf->SetTextColor($severityPal['deep'][0], $severityPal['deep'][1], $severityPal['deep'][2]);
        $pdf->Cell(34, 4.5, 'Tap to call', 0, 1, 'L', false, $telLink);
    }

    $pdf->Ln(1);
}

$decision = is_array($data['decision'] ?? null) ? $data['decision'] : [];
$safePlaces = is_array($data['safePlaces'] ?? null) ? $data['safePlaces'] : [];
$contacts = is_array($data['contacts'] ?? null) ? $data['contacts'] : [];
